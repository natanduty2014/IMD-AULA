#include <iostream>
using namespace std;

int main () {
    int a;
    cin >> a;

    if (a == 0) {
        cout << "zero " << endl;
    }

    if (a == 1) {
        cout << "um " << endl;
    }

    if (a == 2) {
        cout << "dois " << endl;
    }

    if (a == 3) {
        cout << "tres " << endl;
    }

    if (a == 4) {
        cout << "quatro " << endl;
    }

    if (a == 5) {
        cout << "cinco " << endl;
    }

    if (a == 6) {
        cout << "seis " << endl;
    }

    if (a == 7) {
        cout << "sete " << endl;
    }

    if (a == 8) {
        cout << "oito " << endl;
    }

    if (a == 9) {
        cout << "nove " << endl;
    }

return 0;
}
